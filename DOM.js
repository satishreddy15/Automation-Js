//***********DOM STRUCTURE**************
// html -- root element of every HTML document & All webpage content must be inside <html>.
//  ├── head  --  page metadata  --  contains information about the webpage, not the visible content(title, css styles...)
//  │     └── title (Login Page)  --  Defines the name shown in the browser tab & title does not appear inside the webpage, 
//                                    only in the browser tab.
//  │
//  └── body  --  visible page content  --  Everything inside <body> is visible to the user(text, forms, buttons...)
//        └── div  --  container used to group elements & Helps in layout, styling, or scripting.
//              ├── input (id=username)
//              ├── input (id=password)
//              └── button (Login)